import mongoose from "mongoose";

const labServiceSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    description: {
      type: String,
      required: true,
      trim: true,
    },
    price: {
      type: Number,
      required: true,
      min: 0,
    },
  },
  { timestamps: true }
);

export const LabService = mongoose.models.LabService || mongoose.model("LabService", labServiceSchema);
